#include "libft.h"

int	ft_atoi(const char *str)
{
	int				nbr;
	int				sign;
	unsigned int	i;

	i = 0;
	sign = 1;
	nbr = 0;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nbr = (str[i] - 48) + (nbr * 10);
		i++;
	}
	return (nbr * sign);
}
